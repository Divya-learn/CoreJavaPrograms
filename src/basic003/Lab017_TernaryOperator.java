package src.basic003;

public class Lab017_TernaryOperator {
    public static void main(String[] args)
    {

        //Ternary Operator --> condition ? If condition is true print this : If condition is false print this

        int name = true ? 10:20;  //10
System.out.println(name);

        String num = (30>89) ? "Yes" : "No";
        System.out.println(num);
    }
}
