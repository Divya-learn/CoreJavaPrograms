package src.basic001;

public class Lab005_escape_sequence {
    public static void main(String[] args)
    {
        //escape sequence
        char ch1 ='\t'; //tab space
        System.out.println("I am"+ch1+"Learning");
        System.out.println("I am\t learning"); // we can directly add escape sequence(\t,\n,\b,\r) in print statement
        System.out.println("I am\b learning"); //removes one character (m) removed
        System.out.println("I am\r learning"); // removes ( I am)
        System.out.println("I am \"learning\"");//doublequotes
        System.out.println("I am \'learning\'"); //singlequotes

boolean bool = true;
    }
}
